"""Verified present/future verb mini-paradigms for production drills.

These forms are intentionally stored as data, not derived by the rule engine.
Russian present stems mutate too often for a safe beginner-facing heuristic
(писать -> пишу, любить -> люблю, видеть -> вижу), and perfective verbs use
the same six forms with future meaning.
"""

