FORM_KEYS = ("PRES_1SG", "PRES_2SG", "PRES_3SG", "PRES_1PL", "PRES_2PL", "PRES_3PL")


def _forms(*values: str) -> dict[str, str]:
    if len(values) != len(FORM_KEYS):
        raise ValueError(f"expected {len(FORM_KEYS)} forms, got {len(values)}")
    return dict(zip(FORM_KEYS, values))


